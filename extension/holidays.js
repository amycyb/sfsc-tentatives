// YYYY-MM-DD using local-date components (avoids UTC-shift artefacts in TZ ≥ +13)
function localISO(d) {
  return `${d.getFullYear()}-${String(d.getMonth()+1).padStart(2,'0')}-${String(d.getDate()).padStart(2,'0')}`;
}

// Returns ISO date strings (YYYY-MM-DD) for every business day (Mon-Fri, non-holiday) in [from, to]
function weekdaysBetween(from, to) {
  const dates = [];
  const d   = new Date(from + 'T12:00:00');
  const end = new Date(to   + 'T12:00:00');
  while (d <= end) {
    const day = d.getDay();
    if (day !== 0 && day !== 6) {
      const iso = localISO(d);
      if (!COURT_HOLIDAYS.has(iso)) dates.push(iso);
    }
    d.setDate(d.getDate() + 1);
  }
  return dates;
}

// Returns the next business day (weekday, non-holiday) strictly after `isoDate`.
function nextBusinessDay(isoDate) {
  const d = new Date(isoDate + 'T12:00:00');
  do {
    d.setDate(d.getDate() + 1);
  } while (d.getDay() === 0 || d.getDay() === 6 || COURT_HOLIDAYS.has(localISO(d)));
  return localISO(d);
}
